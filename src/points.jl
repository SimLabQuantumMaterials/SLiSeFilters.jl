"FEAST Implementation of Gauss Filters as per the Güttel paper
For multiplier=2, we have quadrupel degrees, for multiplier=1, we have half-degrees
S determines the shape of the ellipse. S->Inf means the unit circle"
function gaussPts( n; multiplier = 2 , S = 1e6 )
    n = multiplier*n;

    # Obtain GL nodes
    x, omega = GaussQuadrature.legendre(n);

    # Map GL nodes from (-1,1) to (0,pi)
    a = 0;
    b = pi;
    theta = (a*(1-x)+b*(1+x))/2;
    omega = omega * (b-a) / 2;

    # Compute mapped nodes and weights according to Güttel, above (3.3)
    z = (S*exp.(im * theta) + 1/S * exp.( - im * theta )) / (S+1/S);
    w = (-omega / 2 / pi) .* (S * exp.(im.*theta) - 1/S * exp.(-im .* theta))/(S+1/S);

    # Take only half of the points due to symmetry
    Z = z[1:Integer(end/2)];
    W = w[1:Integer(end/2)];

    return -conj([Z,W])      # map to other quadrant
end

